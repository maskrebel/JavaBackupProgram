    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Id Psikologi");
        model.addColumn("Kata Istilah Psikologi Klinis");
        model.addColumn("Arti Istilah Psikologi Klinis");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM klinis";
            java.sql.Connection conn =(Connection)Kamus.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3)});
                
            }
            tableistilah.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error: " + e.getMessage());
        }
        
    }

