    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
        
        try{
            String sql = "SELECT kata_istilah , arti_istilah FROM klinis WHERE kata_istilah LIKE '"+kata.getText()+"'";
            
            String url = "jdbc:mysql://localhost/psikologi";
            java.sql.Connection conn =(Connection)Kamus.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                arti.setText(res.getString("arti_istilah"));
            }
           
        }catch(SQLException e){
          JOptionPane.showMessageDialog(null, e);  
        }
        
    }//GEN-LAST:event_btncariActionPerformed

